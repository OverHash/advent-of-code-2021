pub fn get_rotation([x, y, z]: [i32; 3], rot: u8) -> [i32; 3] {
    match rot {
        0 => [x, y, z],
        1 => [y, -x, z],
        2 => [-x, -y, z],
        3 => [-y, x, z],
        4 => [z, y, -x],
        5 => [y, -z, -x],
        6 => [-z, -y, -x],
        7 => [-y, z, -x],
        8 => [z, -x, -y],
        9 => [-x, -z, -y],
        10 => [-z, x, -y],
        11 => [x, z, -y],
        12 => [z, -y, x],
        13 => [-y, -z, x],
        14 => [-z, y, x],
        15 => [y, z, x],
        16 => [z, x, y],
        17 => [x, -z, y],
        18 => [-z, -x, y],
        19 => [-x, z, y],
        20 => [-x, y, -z],
        21 => [y, x, -z],
        22 => [x, -y, -z],
        23 => [-y, -x, -z],
        _ => unreachable!(),
    }
}
